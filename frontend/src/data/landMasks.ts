export const isValidDestination = (maskId: string, x: number, y: number): boolean => {
  const mask = LAND_MASKS[maskId];
  if (!mask) return false;
  const row = Math.floor(y * mask.length);
  const col = Math.floor(x * mask[0].length);
  if (row < 0 || row >= mask.length || col < 0 || col >= mask[0].length) return false;
  return mask[row][col] === '1';
};

export const isOnLandByMask = (maskId: string, x: number, y: number): boolean => {
  return isValidDestination(maskId, x, y);
};
